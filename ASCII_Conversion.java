public class ASCII_Conversion {
    public static void main(String args[])
    {
        char a1,a2,a3;
        a1='a';
        a2='z';
        a3='r';
        int b1=a1;
        int b2=a2;
        int b3='@';
        int b4='s';
        int b5=(int)a3;

        System.out.println("The Ascii value of "+a1+" is "+b1);
        System.out.println("The Ascii value of "+a2+" is "+b2);
        System.out.println("The Ascii value of @ is "+b3);
        System.out.println("The Ascii value of s is "+b4);
        System.out.println("The Ascii value of "+a3+" is "+b5);

        
    }
    
}
